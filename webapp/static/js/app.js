let currentLang = localStorage.getItem('lang') || 'en';

function _tm(key) {
  return (TRANS[currentLang]?.labels?.[key]) ?? (TRANS.en.labels?.[key]) ?? key;
}

function applyI18nModern() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const v = _tm(el.dataset.i18n);
    if (v !== el.dataset.i18n) el.textContent = v;
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const v = _tm(el.dataset.i18nTitle);
    if (v !== el.dataset.i18nTitle) el.title = v;
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const v = _tm(el.dataset.i18nPh);
    if (v !== el.dataset.i18nPh) el.placeholder = v;
  });
  const lb = document.getElementById('m-lang-btn');
  if (lb) lb.textContent = currentLang === 'en' ? 'PL' : 'EN';
}

function toggleLang() {
  currentLang = currentLang === 'en' ? 'pl' : 'en';
  localStorage.setItem('lang', currentLang);
  applyI18nModern();
}

function applyTheme(theme) {
    if (!theme) {
        theme = localStorage.getItem('theme') || 'dark';
    }
    document.body.setAttribute('data-theme', theme);
    localStorage.setItem('theme', theme);
}

document.addEventListener('DOMContentLoaded', () => {
    // Other initializations
    applyTheme();
});

